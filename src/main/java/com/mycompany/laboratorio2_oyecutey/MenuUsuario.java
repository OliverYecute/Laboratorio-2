    private void BTNELIMINARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNELIMINARActionPerformed
    int fila = TABLAREGISTROS.getSelectedRow();

    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione una actividad");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(
        this,
        "¿Eliminar actividad?",
        "Confirmar",
        JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) return;

    String[] seleccionada = listaActual.get(fila);

    List<String[]> todas = ArchivoActividades.cargarActividades();

    for (int i = 0; i < todas.size(); i++) {
        if (java.util.Arrays.equals(todas.get(i), seleccionada)) {
            todas.remove(i);
            break;
        }
    }

    ArchivoActividades.guardarLista(todas);
    cargarRegistros(); // TODO add your handling code here:
    }//GEN-LAST:event_BTNELIMINARActionPerformed

