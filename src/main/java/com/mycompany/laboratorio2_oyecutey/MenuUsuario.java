    private void cargarRegistros() {
    DefaultTableModel modelo = (DefaultTableModel) TABLAREGISTROS.getModel();
    modelo.setRowCount(0);

    listaActual.clear();

    List<String[]> registros = ArchivoActividades.cargarActividades();
    Collections.reverse(registros);

    for (String[] r : registros) {
        if (r.length == 5 && r[1].equals(usuarioActual)) {
            modelo.addRow(r);
            listaActual.add(r);
        }
    }
}

